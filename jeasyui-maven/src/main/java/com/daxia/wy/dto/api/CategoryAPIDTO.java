package com.daxia.wy.dto.api;


public class CategoryAPIDTO extends BaseAPIDTO {
    /**
     * id
     */
    private Long id;
    
    private String name;
    private String type;
    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }
    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getType() {
        return type;
    }
    public void setType(String type) {
        this.type = type;
    }
    
}
